"""
Sync scenario/quest data from RingsDB API → scenarios/ and cycles/ vault directories.

Usage:
    python scripts/sync_scenarios.py
    python scripts/sync_scenarios.py --force
"""

import json
import argparse
from pathlib import Path

import requests
from slugify import slugify

VAULT_ROOT = Path(__file__).parent.parent
SCENARIOS_DIR = VAULT_ROOT / "scenarios"
CYCLES_DIR = VAULT_ROOT / "cycles"
CACHE_DIR = Path(__file__).parent / "cache"

RINGSDB_PACKS_URL = "https://ringsdb.com/api/public/packs/"

CACHE_DIR.mkdir(exist_ok=True)

GENERATED_COMMENT = "<!-- generated by scripts/sync_scenarios.py — do not edit manually -->\n"

# RingsDB has no /cycles/ endpoint — cycle_position on packs encodes the grouping.
# cycle_position values: standalone packs have unique positions; AP cycles share a position.
